void mq_NTT(uint16_t *a, unsigned logn)
{
#pragma HLS INTERFACE m_axi port=a bundle=gmem0 offset=slave depth=1024
#pragma HLS INTERFACE s_axilite port=logn
#pragma HLS INTERFACE s_axilite port=return

#pragma HLS INLINE off
#pragma HLS DATAFLOW

	size_t n, t, m;

	n = (size_t)1 << logn;
	t = n;
	for (m = 1; m < n; m <<= 1) {
		size_t ht, i, j1;

		ht = t >> 1;
		for (i = 0, j1 = 0; i < m; i ++, j1 += t) {
			size_t j, j2;
			uint32_t s;

			s = GMb[m + i];
			j2 = j1 + ht;
			for (j = j1; j < j2; j ++) {
				uint32_t u, v;
				uint32_t tmp1, tmp2;

				u = a[j];
				//v = my_mq_montymul(a[j + ht], s);
				tmp2 = a[j + ht] * s;
				tmp1 = ((tmp2 * Q0I) & 0xFFFF) * Q;
				tmp2 = (tmp2 + tmp1) >> 16;
				tmp2 -= Q;
				tmp2 += Q & -(tmp2 >> 31);
				v = (uint32_t)tmp2;

				//a[j] = (uint16_t)my_mq_add(u, v);
				tmp1 = u + v - Q;
				tmp1 += Q & -(tmp1 >> 31);
				a[j] = (uint16_t)tmp1;

				//a[j + ht] = (uint16_t)my_mq_sub(u, v);
				tmp1 = u - v;
				tmp1 += Q & -(tmp1 >> 31);
				a[j + ht] = (uint16_t)tmp1;

			}
		}
		t = ht;
	}
}
